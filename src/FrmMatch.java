private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    try {
        int a, b;
        a = Integer.parseInt(txt1.getText());
        b = Integer.parseInt(txt2.getText());
        if (a == b) {
            JOptionPane.showMessageDialog(this, "Match Tie Extra Time Given");
        } else {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/HockeyMatchDB", "root", "root");
            String sql = "Insert into MatchMaster(TeamId1,TeamId2,WinningTeamId) values(?,?,?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, TeamId[cmbTeam1.getSelectedIndex()].toString());
            ps.setString(2, TeamId[cmbTeam2.getSelectedIndex()].toString());
            if (a > b) {
                ps.setString(3, TeamId[cmbTeam1.getSelectedIndex()].toString());
                PreparedStatement ups = con.prepareStatement("Update TeamMaster set WinningStatus=0 where TeamId=" + TeamId[cmbTeam2.getSelectedIndex()]);
                ups.executeUpdate();
                JOptionPane.showMessageDialog(this, "Winning Team is " + cmbTeam1.getSelectedItem());
            } else {
                ps.setString(3, TeamId[cmbTeam2.getSelectedIndex()].toString());
                PreparedStatement ups = con.prepareStatement("Update TeamMaster set WinningStatus=0 where TeamId=" + TeamId[cmbTeam1.getSelectedIndex()]);
                ups.executeUpdate();
                JOptionPane.showMessageDialog(this, "Winning Team is " + cmbTeam2.getSelectedItem());
            }
            int rs = ps.executeUpdate();
            if (rs == 1) {
                JOptionPane.showMessageDialog(this, "Match Result Saved");
                cmbTeam1.setSelectedIndex(-1);
                cmbTeam2.setSelectedIndex(-1);
                txt1.setText("");
                txt2.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Not Saved");
            }
        }
    } catch (Exception ex) {
        ex.printStackTrace();
    }
}//GEN-LAST:event_btnSaveActionPerformed

