    void FillTeam() {
        try {
            int i = 0;
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/HockeyMatchDB", "root", "root");
            String sql = "Select count(TeamId) from TeamMaster where WinningStatus = 1";
            PreparedStatement ps = con.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();
            if (rs.next()) {
                int cnt = Integer.parseInt(rs.getString(1));
                if (cnt == 1) {
                    sql = "Select * from TeamMaster where WinningStatus = 1";
                    ps = con.prepareStatement(sql);
                    rs = ps.executeQuery();
                    if(rs.next())
                        JOptionPane.showMessageDialog(this, "Winning Team is " + rs.getString("TeamName"));
                } 
                else {
                    TeamId = new Object[cnt];
                    sql = "Select * from TeamMaster where WinningStatus = 1";
                    ps = con.prepareStatement(sql);
                    rs = ps.executeQuery();
                    i = 0;
                    while (rs.next()) {
                        cmbTeam1.addItem(rs.getString("TeamName"));
                        cmbTeam2.addItem(rs.getString("TeamName"));
                        TeamId[i] = rs.getString("Teamid");
                        i++;
                    }
                    cmbTeam1.setSelectedIndex(-1);
                    cmbTeam2.setSelectedIndex(-1);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

